# Entrada
print("Este programa soma dois números inteiros.\n")
numero_1 = int(input("Digite o primeiro número: "))
numero_2 = int(input("Digite o segundo número: "))

# Processamento
soma = numero_1 + numero_2

# Saída

print(f"\nA soma dos números {numero_1} e {numero_2} é igual a {soma}.")
